
public class Produto {
	String codigo;
	String nome;
	String marca;
	double preco;
	int quantidade;
	
	public Produto() {
		
	}
	
	public Produto(String codigo, String nome, String marca, double preco, int quantidade) {
		this.codigo = codigo;
		this.nome = nome;
		this.marca= marca;
		this.preco = preco;
		this.quantidade  = quantidade;
	}
	
	
}
